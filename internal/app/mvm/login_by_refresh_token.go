package mvm

import (
	"encoding/json"
	"mvm_backend/internal/pkg/errors"
	"mvm_backend/internal/pkg/generated/mvmPb"
	"net/http"
)

func (s *MVMServiceServer) LoginByRefreshToken(w http.ResponseWriter, r *http.Request) {
	var input mvmPb.LoginByRefreshTokenRequest
	if err := json.NewDecoder(r.Body).Decode(&input); err != nil {
		errors.NewHTTPError(w, errors.NewError(err.Error(), http.StatusBadRequest), http.StatusBadRequest)
		return
	}

	res, err := s.service.LoginByRefreshToken(input.RefreshToken)
	if err != nil {
		errors.NewHTTPError(w, errors.NewError(err.Error(), http.StatusBadRequest), http.StatusBadRequest)
		return
	}
	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(mvmPb.LoginByRefreshTokenResponse{
		Token:        res.Token,
		RefreshToken: res.RefreshToken,
	})
}
